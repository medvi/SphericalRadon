function ISRadonExample

    N_phi = 10;
    N_r = 10;
    R0 = 1;
    h_phi = 2*pi / (N_phi + 1);
    h_r = 2 * R0 / N_r;

    % вычисление r_m
    r = zeros(1, N_r+1, 'double');
    for m = 1:N_r+1
        r(m) = (m-1)*h_r;
    end

    % вычисление p_k
    p = zeros(2, N_phi+1, 'double');
    for k = 1:N_phi+1
        p(1, k) = R0*cos((k-1)*h_phi);
        p(2, k) = R0*sin((k-1)*h_phi);
    end

    f = @(x1, x2) x1*x1;
    %result = sradon(f, [0, 0], 1);
    %result
%     f = @(x) x(1)*x(1);
%     % вычисление Mf(p_k, r_m)
    Mf = zeros(N_phi+1, N_r+1);
    for k = 1:N_phi+1
        for m = 1:N_r+1
            Mf(k, m) = sradon(f, [p(1, k), p(2, k)], r(m));
            m;
        end
        k;
    end
    Mf
